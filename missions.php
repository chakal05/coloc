<h2>Nos missions </h2>
  <p> En cette période où notre pays est définitivement dans sa deuxième étape de son existence à savoir l’étape de la construction et du développement national, le RPP proclame, devant l’Histoire son engagement à jouer pleinement son rôle de Parti Politique.Un Parti politique démocratique et moderne, porteur d’espoir et d’idées nouvelles et animé des grands desseins pour son pays et pour les Djiboutiens.
  Héritier des Mouvements nationaux qui ont lutté pour l’indépendance nationale, il a, dès sa création su unir et intégrer en son sein toutes les forces vives de la Nation. Il a ainsi fait sa priorité la consolidation de l’Unité Nationale tout en étant l’artisan des réalisations économiques et sociales qu’a connues notre pays depuis son accession à l’indépendance.
  Fort de cette expérience, notre Parti amorce une mutation qui doit lui permettre de continuer le combat pour vaincre le sous-développement et pour satisfaire les aspirations de la population vers un mieux être social.
  Ses actions actuelles et futures s’inscriront dans le cadre de nouvelles institutions démocratiques et du multipartisme.
  Il saura apporter des solutions justes aux attentes des Djiboutiens. Il défendra les libertés et la démocratie et sera à la première ligne pour défendre des droits de l’Homme. Il reconnaît, respecte et fait sien les principes fondamentaux de l’Islam.
  Au niveau économique et social, le RPP oeuvrera pour que le pays puisse promouvoir une économie libérale fondée sur le développement du secteur des services, des secteurs productifs telles que l’industrie, la pêche et l’agriculture, toute en mettant l’accent sur la nécessité de soutenir les initiatives privées susceptibles de contribuer au développement économique du pays. A ce titre, il soutiendra toutes les activités de recherche et de prospection des ressources naturelles visant à développer le pays.
  Il déploiera des efforts pour parachever la mise en place des infrastructures de base qui constituent les principaux supports de développement.
  D’une manière générale, l’action du Parti visera à promouvoir le rayonnement de notre pays sur le plan régional et international pour affermir son rôle en tant que plaque tournante commerciale et financière.
  Sur le plan social, notre Mouvement s’engagera à développer un système de décentralisation adapté à la réalité nationale afin d’associer plus étroitement les citoyens à la gestion de leur pays et de leur environnement. Le RPP oeuvrera également pour que l’Economie soit au service du bien être de la population et prônera une politique visant à garantir une protection minimale des travailleurs contre les effets pervers de la mondialisation.
  Des efforts seront entrepris pour arriver à une meilleure couverture sanitaire du pays, surtout dans les zones rurales. Dans ce cadre, il renforcera son rôle dans le domaine de la protection de la mère et de l’enfant. La promotion des programmes de vaccination sera parmi ses actions prioritaires.
  Le développement des ressources humaines qui constituent la richesse première de la Nation passe par la réforme du système éducatif. Il conviendra de rechercher une adéquation aussi rapprochée que possible entre le programme de formation professionnelle technique et les besoins exprimés par le marché du travail.
  Le RPP militera en faveur de la promotion du genre et de l’insertion économique et sociale des jeunes ainsi que leur participation au processus décisionnel.
  Parallèlement à notre appartenance à la Communauté francophone, on visera également à développer le système éducatif existant au niveau de l’enseignement général en mettant l’accent sur l’intégration de la langue arabe en vue de renforcer l’appartenance de notre pays au monde arabe avec lequel nous partageons des liens historiques, religieux et culturels.
  Notre mouvement s’efforcera aussi de développer les langues vivantes pour permettre à nos jeunes de mieux maîtriser la technologie avancée et contribuer à la promotion des échanges économiques et commerciaux avec les pays de la région et du reste du monde.
  Au niveau de la politique internationale, le Parti oeuvrera pour la promotion de la paix et de la stabilité régionale sur la base des principes fondamentaux :<br>
  - Le respect de la Coopération;<br>
  - La non-ingérence dans les affaires intérieures;<br>
  - La reconnaissance mutuelle et avantages réciproques.<br>
  Il s’efforcera de promouvoir la coopération économique et commerciale avec le pays de la région, seule voie possible pour assurer la paix et la concorde entre les peuples de la région.
  Le RPP réaffirme son engagement total pour le soutien des causes de la Nation arabe à laquelle nous appartenons.
  Il réitère son soutien indéfectible à la cause palestinienne pour que le peuple frère palestinien puisse recouvrer ses droits légitimes à savoir la création d’un Etat indépendant et souverain sur le sol de la Palestine.</p>

